    public void atualizaTabelaMedico() {
        //atualiza tabela produto
        listaMedico.clear();
        try {
            listaMedico.addAll(regra.getLista());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro" + ex.getMessage());
        }
        int linha = listaMedico.size() - 1;
        if (linha >= 0) {
            tblMedico.setRowSelectionInterval(linha, linha);
            tblMedico.scrollRectToVisible(
                    tblMedico.getCellRect(linha, linha, true));
        }
    }
    
