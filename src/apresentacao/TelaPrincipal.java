    private void btnSalvarMedicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarMedicoActionPerformed
        RegraMedico regra = new RegraMedico();
        int linhaSelecionada = tblMedico.getSelectedRow();
        Medico obj = listaMedico.get(linhaSelecionada);
        try {
            regra.salvarMedico(obj);
            if(obj.getId() == null){
                JOptionPane.showMessageDialog(null, "Medico Cadastrado com êxito");
            }else{
                JOptionPane.showMessageDialog(null, "Medico Alterado com êxito");
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro:" + ex.getMessage());
        }
        trataEdicao(false);
        atualizaTabelaMedico();

    }//GEN-LAST:event_btnSalvarMedicoActionPerformed

