    private void btnExcluirMedicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirMedicoActionPerformed
        int opcao
        = JOptionPane.showOptionDialog(null,
            "Confirma a exclusão?",
            "Pergunta",
            JOptionPane.YES_NO_OPTION,
            JOptionPane.QUESTION_MESSAGE, null,
            new String[]{"Sim", "Não"}, "Sim");
        if (opcao == 0) {
            int linhaSelecionada = tblMedico.getSelectedRow();
            Medico obj = listaMedico.get(linhaSelecionada);
            try {
                regra.removerMedico(obj);
                JOptionPane.showMessageDialog(null, "Medico deletado com êxito");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Erro");
            }
            atualizaTabelaMedico();
        }
    }//GEN-LAST:event_btnExcluirMedicoActionPerformed

